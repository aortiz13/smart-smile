const corsHeaders = {
    'Access-Control-Allow-Origin': '*',
    'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

Deno.serve(async (req) => {
    if (req.method === 'OPTIONS') {
        return new Response('ok', { headers: corsHeaders })
    }

    try {
        const { lead_id, prompt_options } = await req.json()

        // Log request -> In real app, this would trigger a WhatsApp message or queue job
        console.log(`Video Request for Lead ${lead_id}:`, prompt_options)

        return new Response(JSON.stringify({
            success: true,
            message: "Video request queued. Confirmation sent to WhatsApp."
        }), {
            headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        })

    } catch (error) {
        return new Response(JSON.stringify({ error: error.message }), {
            headers: { ...corsHeaders, 'Content-Type': 'application/json' },
            status: 400,
        })
    }
})
